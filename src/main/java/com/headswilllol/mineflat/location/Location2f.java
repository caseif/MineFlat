package com.headswilllol.mineflat.location;

public class Location2f {

	protected float x;
	protected float y;

	public Location2f(float x, float y){
		this.x = x;
		this.y = y;
	}

	public float getX(){
		return x;
	}

	public float getY(){
		return y;
	}

	public void setX(float x){
		this.x = x;
	}

	public void setY(float y){
		this.y = y;
	}

}
